#Aritmatika operation

#prioritas operasi
# 1. ()
# 2. eskponen **
# 3. perkalian dkk * / % //
# 4. pertambahan dan pengurangan + -


a = 10
b = 3

#penambahan +
hasil = a + b
print(a,"+",b, "=", (hasil))

#pengurangan -
hasil = a - b
print(a,"-",b, "=", (hasil))

#perkalian *
hasil = a * b
print(a,"*",b, "=", (hasil))

#pembagian /
hasil = a / b
print(a,"/",b, "=", (hasil))

#eksponen (pangkat) **
hasil = a ** b
print(a,"**",b, "=", (hasil))

#modulus % 10 (dibagi 3 itu berapa sisanya)
hasil = a % b
print(a,"%",b, "=", (hasil))

#floor devision // (kebalikan modulus)
hasil = a // b
print(a,"//",b, "=", (hasil))